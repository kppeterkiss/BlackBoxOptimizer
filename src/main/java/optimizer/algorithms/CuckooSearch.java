package optimizer.algorithms;

import optimizer.common.InternalStateBase;
import optimizer.common.Solution;
import optimizer.param.Param;
import optimizer.trial.IterationResult;
import optimizer.utils.Utils;

import java.lang.Math;
import java.util.*;

import org.apache.commons.math3.special.Gamma;


public class CuckooSearch extends AbstractAlgorithm {
    InternalState state = new InternalState();
    Random rand = new Random();

    {
        this.parallelizable = ParallelExecution.GENERATION;
        this.optimizerParams = new LinkedList<>();
        this.optimizerParams.add(new Param(25,Integer.MAX_VALUE,1,"number_of_nests"));
        // discovery rate of cockoo eggs/solutions
        this.optimizerParams.add(new Param(0.25, 1,0, "Pa"));
        // beta
        this.optimizerParams.add(new Param(1.5, 2,1, "beta"));
        // In most cases, we can use α = O(L/10), where L is the
        //characteristic scale of the problem of interest, whereas in some cases α = O(L/100)
        this.optimizerParams.add(new Param(0.1, 100,0, "alpha"));
        this.optimizerParams.add(new Param(1, 100,0, "random_walk_step_size"));
    }

    private void initSearchSpace(List<Param> parameterMap){
        state.dimension  = parameterMap.size();
        state.lowerBounds = new float[state.dimension];
        state.upperBounds = new float[state.dimension];
        for(int i = 0; i < state.dimension; ++i) {
            state.lowerBounds[i] = ((Number)parameterMap.get(i).getLowerBound()).floatValue();
            state.upperBounds[i] = ((Number)parameterMap.get(i).getUpperBound()).floatValue();
        }
        state.part = AlgorithmPart.second_part;
        state.swarmBestNest = -1;
    }

    private void initNests(int swarmSize) {
        //array to store all parameters
        state.swarm = new ArrayList<>();
        state.calculateResultsForIds = new LinkedList<>();
        //initialize the nests, and add to nest array
        for (int i = 0; i < swarmSize; ++i) {
            state.swarm.add(new Nest(
                    state.dimension,
                    state.lowerBounds,
                    state.upperBounds,
                    rand));
        }
    }

    @Override
    public void updateParameters(List<Param> parameterMap, List<IterationResult> landscape) throws CloneNotSupportedException {
        int numberOfNests = ((Number)optimizerParams.get(0).getValue()).intValue();
        if (state.firstStep) {
            initSearchSpace(parameterMap);
            initNests(numberOfNests);
        } else {
            if (state.part == AlgorithmPart.first_part) {
                /*
                Get a cuckoo randomly
                Generate a solution by L´evy flights [e.g., Eq.(9.2)]
                Evaluate its solution quality or objective value fi
                */
                for (int j = 0; j < numberOfNests; ++j) {
                    //The best solution do not change
                    if (state.swarmBestNest != j) {
                        levy_flights(state.swarm.get(j));
                        state.calculateResultsForIds.add(j);
                    }
                }
            } else if (state.part == AlgorithmPart.second_part){
                float Pa = ((Number)optimizerParams.get(1).getValue()).floatValue();
                double alpha = ((Number)optimizerParams.get(3).getValue()).floatValue();
                float randWalkStepSize = ((Number)optimizerParams.get(4).getValue()).floatValue();
                /*
                A fraction (pa) of worse nests are abandoned
                New nests/solutions are built/generated by Eq.(9.1)
                Keep best solutions (or nests with quality solutions)
                Rank the solutions and find the current best
                */
                for (int i = 0; i < numberOfNests; ++i) {
                    int j = rand.nextInt(state.swarm.size());
                    int k = rand.nextInt(state.swarm.size());
                    float epsilon = rand.nextFloat();

                    if (state.swarmBestNest != i) {
                        for (int l = 0; l < state.dimension; ++l) {
                            state.swarm.get(i).newPosition[l] =
                                    state.swarm.get(i).position[l] + randWalkStepSize *
                                            (float) (alpha * ((Math.signum(Pa - epsilon) + 1) / 2) *  // Heaviside function
                                                    (state.swarm.get(j).position[l] - state.swarm.get(k).position[l]));
                        }
                        state.swarm.get(i).checkBoundsForNewPosition(state.dimension, state.lowerBounds, state.upperBounds);
                        state.calculateResultsForIds.add(i);
                    }
                }
            } else {
                // Should not happen
            }
        }
    }

    public List<Param> createParamSetup(List<Param> pattern, float[] input) throws CloneNotSupportedException {
        List<Param> setup = Param.cloneParamList(pattern);
        // setup each dimension of the position
        for (int i = 0; i < setup.size(); ++i) {
            setup.get(i).setInitValue(input[i]);
        }
        return setup;
    }

    @Override
    public List<List<Param>> getParameterMapBatch(List<Param> pattern)throws CloneNotSupportedException {
        List<List<Param>> result = new LinkedList<>();
        if (state.firstStep) {
            for (Nest nest : state.swarm) {
                result.add(createParamSetup(pattern, nest.position));
            }
        } else {
            state.calculateResultsForIds.forEach((Integer id) -> {
                try {
                    result.add(createParamSetup(pattern, state.swarm.get(id).newPosition));
                } catch (CloneNotSupportedException e) {
                    e.printStackTrace();
                }
            });
        }
        return result;
    }

    public void setResults(List<IterationResult> results) throws CloneNotSupportedException {
        if (state.firstStep) {
            int i = 0;
            for (IterationResult res : results) {
                state.swarm.get(i).actualFitness = res;
                if(state.swarm.get(i).actualFitness.betterThan(state.swarmBestFitness)) {
                    state.swarmBestFitness = state.swarm.get(i).actualFitness;
                    state.swarmBestKnownPosition = state.swarm.get(i).position.clone();
                    state.swarmBestNest = i;
                }
                ++i;
            }
        } else {
                //rewrite it, not really good in this form, prone to error
                int i = 0;
                for (IterationResult res : results) {
                    int id = state.calculateResultsForIds.get(i);
                    Nest nest = state.swarm.get(id);
                    if (nest.actualFitness.betterThan(res)) {
                        nest.actualFitness = res;
                        nest.position = nest.newPosition.clone();

                        if(nest.actualFitness.betterThan(state.swarmBestFitness)) {
                            state.swarmBestFitness = nest.actualFitness;
                            state.swarmBestKnownPosition = nest.position.clone();
                            state.swarmBestNest = i;
                        }
                    }
                    ++i;
                }
        }
    }
    /*
    * This method sets for a nest a new position, in the
    * newPosition attribute.
    * */
    public void levy_flights(Nest nest) {
        double beta = ((Number)optimizerParams.get(2).getValue()).floatValue();

        double tmpdiv = Gamma.gamma((1 + beta) / 2) * beta * Math.pow(2,((beta - 1) / 2));
        double sigma = Math.pow((Gamma.gamma(1 + beta) * Math.sin(Math.PI * beta / 2)) / tmpdiv, 1 / beta) ;

        // Levy flights by Mantegna's algorithm
        double[] step = new double[state.dimension];
        for (int i = 0; i < state.dimension; ++i) {
            // u / (v)^(1/beta)
            step[i] = (rand.nextGaussian() * sigma) / Math.pow(Math.abs(rand.nextGaussian()), 1 / beta);
            }

        double alpha = ((Number)optimizerParams.get(3).getValue()).floatValue();
        //stepSize = alpha * step[i] * (s - best);
        for (int i = 0; i < state.dimension; ++i) {
            nest.newPosition[i] = nest.position[i] + (float) (alpha * step[i] * (nest.position[i] - state.swarmBestKnownPosition[i]));
        }
        nest.checkBoundsForNewPosition(state.dimension, state.lowerBounds, state.upperBounds);
    }

    public void updateGlobals() throws CloneNotSupportedException {
        state.firstStep = false;
         if (state.part == AlgorithmPart.first_part)
             state.part = AlgorithmPart.second_part;
         else
             state.part = AlgorithmPart.first_part;
         state.calculateResultsForIds.clear();
    }

    class Nest extends Solution {
        public Nest(int dim,  float[] lowerBounds, float[] upperBounds, Random rand) {
            super(dim, lowerBounds, upperBounds, rand);
        }
    }

    enum AlgorithmPart {
            first_part,
            second_part;
    }

    class InternalState extends InternalStateBase<Nest> {
        int swarmBestNest;
        List<Integer> calculateResultsForIds;
        AlgorithmPart part;
    }
}
